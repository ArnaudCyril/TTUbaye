Le Tennis de Table de l’Ubaye est une association sportive née en 1985. Sa parution au Journal Officiel date du 2 janvier 1985 et ses statuts ont été déposés en préfecture le 08 juillet 1986.</br>
Il a reçu l’agrément de la direction de la Jeunesse et des Sports, numéro 861750 et exerce son activité  avec le numéro SIRET 44875552000013. </br>
L’association a été affiliée à la Fédération Française de Tennis de Table en 1986, ce qui lui a permis d’inscrire cette année là une départementale 1 et 2.</br>
Son siège social est situé à la salle Omnisport, allée des Dames à Barcelonnette.</br>
Le Comité Directeur est actuellement composé de Gilles Damien Président, Lucas Garino Vice-président, Daniel Friggi Trésorier, Sabine Trens, Secrétaire, Frédéric Mercereau et Jouarie Pierre-Philippe Secrétaire Adjoint, Arnaud Cyril Responsable Web, Albert Berton Membre.</br>
