		<span style="font-size:24;font-weight:bold;">Histoire :</span>
		
		<p></br>
		Le TTU a commencé avec environ 70 adhérents avec à sa présidence, Claude MASSE, au secrétariat, Georges FARJAS à la trésorerie, André FRIGGI et à l’entraînement Fabien BICHET.
Au départ, le club évoluait dans la salle des Fêtes de Saint Pons où il était difficile de s’entraîner dans la mesure où il fallait faire cas d’un sol carrelé et glissant surtout les lendemains de bals. </br></br>

En 1987, il a eu l’opportunité de s’installer dans l’ancien hôpital de Barcelonnette, l’actuelle médiathèque. La configuration n’était pas optimale car il n’y avait qu’un mètre de recul derrière les joueurs mais c’était « leur » salle. Tous les licenciés ont été mis à contribution pour mettre en conformité une salle abandonnée. 
</br></br>
En 1990, la construction de la Salle Omnisports de Barcelonnette terminée, le TTU a eu deux créneaux de 2 heures et la possibilité de s’entraîner de façon plus académique. C’est grâce à cet entrainement adéquat que l’équipe fanion a pu accéder à la régionale 2. 
Il payait alors une location de la salle d’un montant presque équivalent à la subvention de la mairie de Barcelonnette. 
</br></br>
En 1992, les licenciés ont eu 3 créneaux de 2 heures et la certitude de pouvoir accueillir des compétitions le week-end de championnat et de Critérium Fédéral. 
</br></br>
En 1994, le club accueille un tour de Nationale 2 du Critérium Fédéral.
</br></br>
Le club a tenté l’aventure en régionale 1 avec tout d’abord le club de Gap et ensuite en 2007 avec celui de Briançon. 
</br></br>
En 2011, le TT Ubaye accèdera cette fois tout seul, à la Régionale 1.
</br></br>
Jusqu'en mai 2013 nous disposions d'une demi salle omnisports, car partagée avec le tir à l'arc, le lundi de 18h30 à 20h00 et le mercredi de18h00 à 20h00 et de la salle entière le vendredi de 18h00 à 20h00. Ce jour là, nous accueillions sur 20 tables, 4 groupes différents qui correspondaient à quatre niveaux de pratique et d’âge: notre élite jeune, les séniors et jeunes loisirs, la section baby ping, et les jeunes du PEL.
</br></br>
<span style="font-weight:bold">La section élite ou compétition</span></br>
Dans ce groupe les joueurs pouvaient s’entraîner 6h00 par semaine, ce qui correspond à un entraînement de joueurs évoluant en régionale. Certains jeunes étaient obligés de compléter leur entraînement par des CPS et des stages car leur volume d’entraînement était insuffisant pour prétendre à des résultats nationaux. 
</br></br>
<span style="font-weight:bold">La section loisir jeunes</span></br>
La section loisir jeunes avait le même entraînement que la section élite mais avait un volume horaire réduit à 2 heures.
</br></br>
<span style="font-weight:bold">La section loisir adultes</span></br>
La section loisir, concernait un public adulte et on n’apportait pas les mêmes contenus pédagogiques que ceux de la section élite. Il s’agissait d’un créneau qui alliait pratiques pongistes accessibles à tous et activités conviviales et qui permettait d’entretenir sa bonne forme physique.
</br></br>
<span style="font-weight:bold">La section baby ping</span></br>
La section baby ping s’adressait aux enfants de 4 à 7 ans pendant 1 heure sous forme d’ateliers ludiques.
</br></br>
Durant ces 28 dernières années, de nombreux sportifs ont pratiqué le Tennis de Table  au sein du TT Ubaye. Laurent Petrucci en Juniors a participé aux championnats de France. Rémi Gosselin est devenu champion de France par classement en 1997. Benjamin et Quentin Friggi ont intégré l’équipe PACA aux Championnat de France des Régions à Ceyrat, des Mini-Inter Ligues au Puy en Velay, des Inter-Comités à la Roche sur Foron en 2010.
Quentin Friggi a obtenu le titre de Champion de Provence Poussins en 2007, celui de Benjamins en 2008, et de Minimes en 2010. Colin FRIGGI est devenu en 2011,vice-champion de Provence en Benjamin.
Deux jeunes, Guilain Rougon et Antoine Knoups formés au club sont maintenant brevets d’état 1er degré et Benjamin FRIGGI est rentré depuis le mois de septembre en formation DEJEPS. 
		</p>
		
		
		
		
		
