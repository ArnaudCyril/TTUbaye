		<span style="font-size:24;font-weight:bold;">Les valeurs du TT UBaye :</span>
		<p>
		Le TT Ubaye est une association qui vise le développement de la jeunesse ou l’épanouissement des moins jeunes par la pratique du Tennis de Table. </br>
Elle est tournée vers la compétition individuelle ou par équipes mais se veut également une association accueillant tous ceux qui souhaitent s’orienter vers une pratique de loisir, en contribuant à l’effort commun d’éducation et de socialisation par le sport. </br>
Dès leur arrivée au club, les enfants apprennent un certain nombre de valeurs, comme l’attention, la politesse, le respect des règles et de l’autre, avant même d’apprendre les gestes techniques qui feront d’eux les champions de demain. </br>
Plus tard, ils apprendront l’autonomie, la persévérance, la coopération, le soutien de l’autre avec l’ambition d’aller plus loin dans la connaissance d’eux-mêmes, et l’espoir de quitter le club mieux armés pour l’avenir. </br>
Les valeurs prônées y sont : </br></br>

1. L’esprit d’équipe, la discipline, la coopération, le respect des règles et de l’autre. </br>
2. La maîtrise de son corps et le développement d’une condition physique optimale. </br>
2. Le goût de l’effort, du travail individuel et de l’atteinte progressive de ses objectifs. </br>
4. Une construction de la personnalité et de la confiance en soi au travers des succès, des échecs et du contrôle de ses émotions. </br></br>

Pour atteindre ces objectifs, le club s’entoure de bénévoles et depuis le mois de novembre d'un salarié en contrat d'avenir dont la mission est de faire progresser en prenant du plaisir.
		</p>
