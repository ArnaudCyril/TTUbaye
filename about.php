		<span style="font-size:24;font-weight:bold;">La situation actuelle (janvier 2014) :</span>
		<p>
		Notre Commune, ayant fait le rachat du quartier militaire « Craplet » pour y transférer la Cité scolaire (collège et lycée avec internat d'excellence, classes sportives ski alpin, ski nordique), accueillir des entreprises innovantes,  un pôle de recherches scientifiques risques majeurs et biodiversité (Les Séolanes), une école d'ébénisterie, les pompiers, une blanchisserie avec des employés handicapés..., a proposé au club un hangar de 19 m 50 sur 16 m 50 (aires de jeu de 240 m2 et des vestiaires) qu’il a aménagé grâce à ses bénévoles et à la municipalité.</br>
Pour le club, cette solution a été salvatrice car cela lui permet aujourd'hui de disposer d’une salle permanente et adaptée avec 8 aires de jeu pour l’entraînement et d’accueillir un public plus nombreux. </br>
Notre objectif à moyen terme est de doubler notre effectif, en proposant pendant la pause méridienne et les fins d'après midi de 16h00 à 22h00 des entraînements adaptés à tous les publics (féminines, personnes à mobilité réduite, loisirs, compétiteurs et l'élite jeune qui a enfin un accroissement de son volume d’entraînements....). </br>
</br>
Dans le cadre de l’aménagement du temps de l’enfant dès la rentrée 2014, on envisage aussi de permettre l'accès aux enfants des écoles primaires les après midis et au collège et lycée qui s'installeront si tout va bien sur le site en 2015/2016. </br>
La création d'une classe sportive "tennis de table" est d'ailleurs en discussion avec le Proviseur du Lycée et le Maire de Barcelonnette. Les étudiants profiteraient des horaires aménagés des sections sportives. </br></br>

Dans le futur on aimerait ouvrir la salle entre 12h00 et 14h00 aux salariés des différentes entreprises, étudiants  chercheurs et pompiers installés intra muros. </br></br>

Malheureusement la température de 8° dans la salle cet hiver n'a pas permis de continuer à nous entraîner à Craplet. C'est ainsi qu'à la mi décembre nous avons réintégré la salle omnisport,  avec ses désagréments mais aussi avec l'assurance de pouvoir accueillir décemment nos adhérents et les équipes en compétition.</br></br> 

Mi janvier un plafond est en cours de réalisation afin que nous puissions chauffer un minimum notre salle. </br></br>

Aujourd’hui il y a environ 55 adhérents pour un nombre de compétiteurs resté stable.
Le club est surtout constitué de jeunes. Le changement de salle et les plannings incertains de début de saison ont contribué à une légère baisse des licenciés. </br>
Le club a formé des licenciés à l'arbitrage. Il y a entres autres des arbitres régionaux, 3 Juges-Arbitres 1° et un juge-arbitre 2°.</br></br>

Actuellement il y a 5 groupes d'entraînement différents  :</br></br>

<span style="font-weight:bold">La section élite ou compétition </span></br>
Dans ce groupe les joueurs peuvent dorénavant s’entraîner 12h00 par semaine contre 6 auparavant. De 18 à 22 heures le mardi, le mercredi et le vendredi.</br></br>

<span style="font-weight:bold">La section jeune élite </span></br>
Dans ce groupe les jeunes peuvent s’entraîner 11h00 par semaine. De 17 à 20h le mardi, de 16 à 20 heures  le mercredi, de 18h à 20 heures le vendredi et de 10 à 12 heures le samedi.</br></br>

<span style="font-weight:bold">La section loisir jeunes et adultes</span></br>
La section loisir, s’adresse à un public de jeunes et d'adultes hommes et femmes qui profite de 8 heures d'entraînement le lundi et le jeudi de 18 à 22 heures, avec dorénavant un contenu pédagogique. C'est toujours un créneau qui allie pratiques pongistes accessibles à tous et activités conviviales.
</br></br>
<span style="font-weight:bold">La section baby ping</span></br>
La section baby ping s’adresse aux enfants de 4 à 7 ans pendant 2 heures sous formes d’ateliers ludiques le mercredi de 14 à 15 heures et le vendredi de 17 à 18 heures.
</br></br>
<span style="font-weight:bold">La section ping chounes</span></br>
La section ping chounes s’adresse aux enfants de 7 à 10 ans pendant 3 heures sous formes d’ateliers ludiques et d'entraînement adapté, le mercredi de 15 à 16 heures et le samedi de 10 à 12 heures.
</br></br>
Nous organisons chaque année des compétitions départementales, régionales voire nationales. Cette saison nous avons accueilli entres autres la phase régionale PACA des finales par classement, dernière phase qualificative avant la finale nationale et le 1er tour du Critérium fédéral jeunes. 
